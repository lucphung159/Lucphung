type PublicationSectionRecord = Record<string, unknown>;

function isObject(value: unknown): value is PublicationSectionRecord {
  return typeof value === "object" && value !== null;
}

export function needsPublicationSeed(publicationSections: unknown) {
  if (!Array.isArray(publicationSections)) return true;

  return publicationSections.every((section) => {
    if (!isObject(section)) return true;
    const publicationsList = section.publicationsList;
    return !Array.isArray(publicationsList) || publicationsList.length === 0;
  });
}

function sectionLooksLikeSelectedPapers(section: PublicationSectionRecord) {
  const title = String(section.title || "").toLowerCase();
  if (
    title === SELECTED_PAPERS_SECTION_TITLE.toLowerCase() ||
    title.includes("selected publication") ||
    title.includes("publications (*corresponding author)")
  ) {
    return true;
  }

  const publicationsList = section.publicationsList;
  if (!Array.isArray(publicationsList)) return false;

  return publicationsList.some((publication) => {
    if (!isObject(publication)) return false;
    const paperTitle = String(publication.title || "").toLowerCase();
    return paperTitle.includes("three-year impacts of composted sewage sludge") ||
      paperTitle.includes("growth, yield and silicon uptake of rice");
  });
}

export function getDefaultPublicationSections() {
  return defaultPublicationSections.map((section) => ({
    ...section,
    publicationsList: section.publicationsList.map((publication) => ({
      ...publication,
      links: [...publication.links],
    })),
  }));
}

export function syncPublicationSections(publicationSections: unknown) {
  const [selectedPapersSection] = getDefaultPublicationSections();

  if (needsPublicationSeed(publicationSections)) {
    return { publicationSections: [selectedPapersSection], changed: true };
  }

  if (!Array.isArray(publicationSections)) {
    return { publicationSections: [selectedPapersSection], changed: true };
  }

  const sections = publicationSections.map((section) => (
    isObject(section) ? { ...section } : section
  ));
  const selectedIndex = sections.findIndex((section) => (
    isObject(section) && sectionLooksLikeSelectedPapers(section)
  ));

  if (selectedIndex === -1) {
    return { publicationSections: [selectedPapersSection, ...sections], changed: true };
  }

  const currentSection = sections[selectedIndex];
  if (
    isObject(currentSection) &&
    currentSection.sourceVersion === SELECTED_PAPERS_SOURCE_VERSION
  ) {
    return { publicationSections: sections, changed: false };
  }

  const currentNote = isObject(currentSection) && typeof currentSection.note === "string"
    ? currentSection.note
    : "";

  sections[selectedIndex] = {
    ...selectedPapersSection,
    note: currentNote || selectedPapersSection.note,
  };

  return { publicationSections: sections, changed: true };
}
